uint64_t GetMillis(void)
{
	uint32_t cnt;
	uint64_t Millis_return;

	cnt = TIM7->CNT;
	Millis_return = glo_50ms * 50 + cnt/1000;

	return Millis_return;
}

/*******************************************************************************
* 函数?? : GetMicros()
* ??  ?? : 获取系统时间微秒
* ??  ?? : ??
* ??  ?? : ??
* 返回?? : 系统时间微秒
* ??  ?? : ??
*******************************************************************************/
uint64_t GetMicros(void)
{
	uint16_t cnt = TIM7->CNT;
	return glo_50ms * 50000 + cnt;
}

uint8_t Wait_processing(uint16_t TMS)
{
	if(TIM6->CNT<TMS)			 	 //等待是否到达定时时间
	{		
		return 1;			 		 //未到时间则返回等待标致。
	}
	else
	{
		TIM6->CNT=0;			  	 //到达定时时间则清除定时器计数器
		return 0;
	}
}  

